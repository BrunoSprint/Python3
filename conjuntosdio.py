''' 
Criando sets : Um set é uma coleção que não possui objetos repetidos, usamos dets para representar conjuntos matemáticos ou eliminar itens duplicados de um iterável. 
'''


set([1, 2, 3, 1, 3, 4])    #[1, 2, 3, 4]

set("abacaxi")    #{"b", "a", "c", "x", "i"}


set(("palio", "gol", "celta", "palio"))  #{"gol", "celta", "palio"}





